<p>"Милитаризм" - военно-стратегическая пошаговая игра класса PBEM (play by e-mail - предназначенная для игры по электронной почте). Кроме режима игры по электронной почте, в игре предусмотрен также режим "Hot seat", при котором несколько человек могут проводить игру на одном локальном компьютере. В игре может принимать участи до десяти человек, каждый из которых представляет отдельную расу. Игра не ориентирована строго на какой-либо исторический период, в ней присутствуют все элементы и войска, характерные для двадцатого века. Боевые характеристики и тактика усреднены для этого периода.</p>

